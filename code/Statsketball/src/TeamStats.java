
public class TeamStats extends EntityInfo {

	private double pts;
	private int wins;
	/**
	 * @return the pts
	 */
	public double getPts() {
		return pts;
	}
	/**
	 * @param pts the pts to set
	 */
	public void setPts(double pts) {
		this.pts = pts;
	}
	/**
	 * @return the wins
	 */
	public int getWins() {
		return wins;
	}
	/**
	 * @param wins the wins to set
	 */
	public void setWins(int wins) {
		this.wins = wins;
	}
}
